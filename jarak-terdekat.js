function jarakTerdekat(arr) {
  //code here
  let indexI = [];
  let indexJ = [];

  for(let i = 0; i < arr.length; i++) {
    for(let j = 0; j < arr[i].length; j++) {
      if(arr[i][j] == 'X') {
        indexI.push(i);
        indexJ.push(j);
      }
    }
  }
  if(indexI.length < 2) {
    return 0;
  }
  let total1 = indexI[1] - indexI[0];
  let total2 = Math.abs(indexJ[1] - indexJ[0]);
  let result = total1 + total2;
  return result
}

